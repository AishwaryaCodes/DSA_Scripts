console.log(0 == false);
console.log(0 === false); // 0 is num, and false is boolean

console.log(1 == true);
console.log(1 === true); // 1 is num and true is boolean

console.log(null == undefined);
console.log(null === undefined); // null is type of object, and undefined is type of undefined


console.log(NaN == NaN); // always
console.log(NaN === NaN);

console.log(isNaN(NaN)); // true
console.log(Number.isNaN(NaN));

console.log([] == []); //false
console.log({} == {});

